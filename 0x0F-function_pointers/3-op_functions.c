#include "3-calc.h"

/**
 * op_add - sums two integers
 * @a: integer
 * @b: integer
 *
 * Return: sum of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two integers
 * @a: integer
 * @b: integer
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mulitiplies two integers
 * @a: integer
 * @b: integer
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two integers
 * @a: integer
 * @b: integer
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - divides two integers
 * @a: integer
 * @b: integer
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
